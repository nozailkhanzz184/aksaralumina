const content = `import React, { createContext, useContext, useState, useEffect } from 'react';

type Language = 'id' | 'en' | 'zh' | 'es' | 'hi' | 'ar' | 'bn' | 'pt' | 'ru' | 'ja';

const translations = ${JSON.stringify(translations, null, 2)};

type I18nContextType = {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: string) => string;
};

const I18nContext = createContext<I18nContextType>({
  lang: 'id',
  setLang: () => {},
  t: (key) => key,
});

export const I18nProvider = ({ children }: { children: React.ReactNode }) => {
  const [lang, setLang] = useState<Language>(() => {
    return (localStorage.getItem('app-lang') as Language) || 'id';
  });

  useEffect(() => {
    localStorage.setItem('app-lang', lang);
  }, [lang]);

  const t = (key: string) => {
    return translations[lang][key as keyof typeof translations['id']] || key;
  };

  return (
    <I18nContext.Provider value={{ lang, setLang, t }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useI18n = () => useContext(I18nContext);
`;

